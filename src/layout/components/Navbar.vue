<template>
  <div class="navbar">
    <hamburger :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />

    <breadcrumb class="breadcrumb-container" />

    <div class="right-menu">
      <el-dropdown class="avatar-container" trigger="click">
        <div class="avatar-wrapper">
          <el-tooltip class="item" effect="dark" :content="userName" placement="left">
            <div class="user-avatar">{{ avatarName }}</div>
          </el-tooltip>
          <i class="el-icon-caret-bottom" />
        </div>
        <el-dropdown-menu slot="dropdown" class="user-dropdown">
          <router-link to="/">
            <el-dropdown-item>
              首页
            </el-dropdown-item>
          </router-link>
          <el-dropdown-item divided @click.native="logout">
            <span style="display:block;">退出登录</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'

export default {
  components: {
    Breadcrumb,
    Hamburger
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'userName'
    ]),
    avatarName() {
      // 检查 this.userName 是否为空
      if (!this.userName || this.userName.length === 0) {
        return '';
      }

      // 判断是否全是英文
      if (/^[A-Za-z]+$/.test(this.userName)) {
        // 如果是，取首字母并转大写
        return this.userName.charAt(0).toUpperCase();
      }

      // 判断是否全是中文
      if (/^[\u4e00-\u9fa5]+$/.test(this.userName)) {
        // 如果是，取最后一个字符
        return this.userName.charAt(this.userName.length - 1);
      }

      // 如果都不是，取第一个字符
      let firstChar = this.userName.charAt(0);

      // 如果第一个字符是英文，转大写
      if (/[A-Za-z]/.test(firstChar)) {
        return firstChar.toUpperCase();
      }

      // 否则直接返回第一个字符
      return firstChar;
    }
  },
  methods: {
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      await this.$store.dispatch('user/logout')
      this.$router.push(`/login?redirect=${this.$route.fullPath}`)
    }
  }
}
</script>

